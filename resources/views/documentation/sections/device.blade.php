            <h4>Simple Device</h4>

            <p>In this tab we will learn how to connect simple device to website to send data for it .</p>

            <p>After add new device to website we need to connect it to this website by make device send post request to
                the website using this url :</p>

            <div class="intro2 clearfix">
                <p style="text-align: center;font-size: 25px;font-weight: bold">https://dodolora.com/api/create

                </p>
            </div>

            <p>The device must send data in this format</p>

            <pre class="brush: html; highlight: [1,3]">
                "DeviceID,parameter 1 value ,parameter 2 value,parameter 3 value,.............,date and time of read in format like this 2022-04-03 11:47"

                as example "GL34PT3SD2,4,3,5,2,3,6,3.5,4,6,2022-04-03 11:47"

                in this example the device id is GL34PT3SD2 and this numbers 4,3,5,2,3,6,3.5,4,6 is the parameter values and this 2022-04-03 11:47 is the time of read

                the date and time of read is optional if the device do not send it the website will set is as the time of server like this :

                "GL34PT3SD2,4,3,5,2,3,6,3.5,4,6"
            </pre>

            <p>If the device type need response the website will send this response which is the setting value of device with date</p>

            <pre class="brush: html">
                "##S1=15,T1=17,TH=25,SP1=14,SP2=29,time=2022-10-13GMT10:06:59"

                S1 and T1 and TH and SP1 and SP2 is the setting of this device type and the value of them is the device setting value

                time is the time of server in GMT

                this response will send with 201 code
                         </pre>

            <p>if the website can not find the device Id then it will send response like "device id is not exist"  with 404 code</p>

            <pre class="brush: html">
                            "device id is not exist"
                         </pre>
